#!/usr/bin/constants
""" Collection of physical constants

Main source: http://physics.nist.gov/cuu/Constants/index.html
"""

def AMU():
    """ Atomic mass unit, kg """
    return 1.660538782e-27

def AVOGADRO():
    """ Avogadro constant, mol^-1 """
    return 6.02214179e23

def BOLTZMANN():
    """ Boltzmann constant, J K^-1 """
    return 1.3806504e-23

def ELECTRONMASS():
    """ Electron mass, kg """
    return 9.10938215e-31

def ELEMENTARYCHARGE():
    """ Elementary charge, C """
    return 1.602176487e-19
    
def SPEEDOFLIGHT():
    """ Speed of light in vacuum, m s^-1 """
    return 299792458
